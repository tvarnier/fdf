#include "libft.h"

int		ft_getnbr(char *str)
{
	int		i;
	int		nbr;

	nbr = 0;
	i = 0;
	if (str[0] == '+' || str[0] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	if (str[0] == '-')
		nbr = nbr * (-1);
	return (nbr);
}
